package organicPets;
public class OrganicDog extends Organic {

	public OrganicDog(String name, int hungerLevel, int thirstLevel, int happyLevel, int healthLevel, int messLevel) {
		this.name = name;
		this.hungerLevel = hungerLevel;
		this.thirstLevel = thirstLevel;
		this.happyLevel = happyLevel;
		this.healthLevel = healthLevel;
		this.messLevel = messLevel;
	}


}
